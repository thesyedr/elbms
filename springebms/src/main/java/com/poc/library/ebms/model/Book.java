package com.poc.library.ebms.model;

import java.util.Date;

public class Book {
	
	private int book_id;
	private String book_title;
	private String book_author;
	private String book_edition;
	private String book_publisher;
	private String book_arrival_date;
	private double book_price;
	private String book_category;
	
	public int getBook_id() {
		return book_id;
	}
	public void setBook_id(int book_id) {
		this.book_id = book_id;
	}
	public String getBook_title() {
		return book_title;
	}
	public void setBook_title(String book_title) {
		this.book_title = book_title;
	}
	public String getBook_author() {
		return book_author;
	}
	public void setBook_author(String book_author) {
		this.book_author = book_author;
	}
	public String getBook_edition() {
		return book_edition;
	}
	public void setBook_edition(String book_edition) {
		this.book_edition = book_edition;
	}
	public String getBook_publisher() {
		return book_publisher;
	}
	public void setBook_publisher(String book_publisher) {
		this.book_publisher = book_publisher;
	}
	public String getBook_arrival_date() {
		return book_arrival_date;
	}
	public void setBook_arrival_date(String book_arrival_date) {
		this.book_arrival_date = book_arrival_date;
	}
	public double getBook_price() {
		return book_price;
	}
	public void setBook_price(double book_price) {
		this.book_price = book_price;
	}
	public String getBook_category() {
		return book_category;
	}
	public void setBook_category(String book_category) {
		this.book_category = book_category;
	}
	
	
	
	
	

}
